package model;

import java.io.Serializable;
import java.util.Objects;

/**
 *
 * @author mannj14
 */
public class Gauntlets implements Serializable{
    
    public String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 29 * hash + Objects.hashCode(this.name);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Gauntlets other = (Gauntlets) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Gauntlets{" + "name=" + name + '}';
    }
        
    
}
